package Level_1_While_DoWhile_For;

public class Task_013_For {

// Рисуем треугольник;

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("8");
            }
            System.out.println("8");
        }
    }
}

